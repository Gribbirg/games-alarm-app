package com.example.smartalarm.feature.games.reverse.logic

/**
 * Словари русских слов для игры «Наоборот».
 *
 * Принципы составления:
 * - только существительные в именительном падеже единственного числа;
 * - повседневная лексика без редких, устаревших или специальных слов —
 *   спросонья игрок должен узнавать слово сразу;
 * - строчные буквы, буква «ё» не заменяется на «е»;
 * - длина слова строго соответствует уровню сложности:
 *   [EASY] — 4–5 букв, [MEDIUM] — 6–7 букв, [HARD] — 8 и более букв;
 * - в каждом списке не меньше 25 слов и нет дубликатов;
 * - нет палиндромов: слово, прочитанное задом наперёд, никогда не совпадает
 *   с ответом, иначе раунд решался бы без чтения;
 * - слова подобраны «семьями» с общими буквами (коса/коза/кора,
 *   корова/ворона/сорока), чтобы у похожих слов находились
 *   правдоподобные дистракторы.
 */
object ReverseWords {

    /** Уровень 1: короткие слова из 4–5 букв. */
    val EASY: List<String> = listOf(
        "коса", "коза", "кора", "гора", "нора",
        "роса", "лиса", "вода", "сода", "мода",
        "рука", "река", "мука", "щука", "море",
        "поле", "роль", "соль", "боль", "полка",
        "палка", "белка", "булка", "банка", "галка",
        "марка", "маска", "миска", "мышка", "мишка",
        "майка", "чайка", "лампа", "лапша"
    )

    /** Уровень 2: слова средней длины из 6–7 букв. */
    val MEDIUM: List<String> = listOf(
        "корова", "ворона", "сорока", "корона", "дорога",
        "погода", "молоко", "болото", "золото", "работа",
        "забота", "ворота", "минута", "машина", "малина",
        "калина", "монета", "ракета", "газета", "планета",
        "картина", "корзина", "капуста", "колбаса", "конфета",
        "котлета", "комната", "команда", "письмо", "яблоко",
        "облако", "молоток", "потолок", "гитара", "собака"
    )

    /** Уровень 3: длинные слова из 8 и более букв. */
    val HARD: List<String> = listOf(
        "квартира", "лестница", "карандаш", "апельсин", "мандарин",
        "виноград", "институт", "праздник", "прогулка", "ресторан",
        "вертолёт", "простыня", "карусель", "будильник", "телевизор",
        "компьютер", "велосипед", "картофель", "остановка", "программа",
        "профессия", "космонавт", "полотенце", "сковорода", "мороженое",
        "бутерброд", "автомобиль", "расписание", "фотография", "библиотека",
        "тренировка", "настроение", "холодильник", "путешествие"
    )

    /**
     * Возвращает словарь для уровня сложности.
     *
     * @param difficulty уровень сложности 1..3; значения вне диапазона
     * приводятся к ближайшему уровню.
     */
    fun forDifficulty(difficulty: Int): List<String> = when {
        difficulty <= 1 -> EASY
        difficulty == 2 -> MEDIUM
        else -> HARD
    }
}
