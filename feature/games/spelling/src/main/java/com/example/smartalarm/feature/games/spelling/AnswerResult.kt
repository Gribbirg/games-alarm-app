package com.example.smartalarm.feature.games.spelling

/**
 * Результат ответа игрока на вопрос игры «Как пишется?».
 */
enum class AnswerResult {
    /** Ответ верный, но игра ещё не закончена — показан следующий вопрос. */
    CORRECT,

    /** Ответ неверный — показан следующий вопрос (другое слово). */
    WRONG,

    /** Ответ верный и набрано нужное число верных ответов — победа. */
    WIN
}
