package com.example.smartalarm.feature.games.spelling

/**
 * Одна запись словаря игры «Как пишется?».
 *
 * Все варианты написания (правильный и неправильные) составлены вручную
 * заранее — игра НЕ генерирует ошибки алгоритмически.
 *
 * @property correct эталонное (правильное) написание слова или словосочетания
 * @property wrong список из 1–3 неправильных вариантов написания того же слова;
 * ни один из них не совпадает с [correct], и все они уникальны между собой
 */
data class SpellingEntry(
    val correct: String,
    val wrong: List<String>
)
