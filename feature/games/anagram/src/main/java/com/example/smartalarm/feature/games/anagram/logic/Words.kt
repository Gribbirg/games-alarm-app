package com.example.smartalarm.feature.games.anagram.logic

/**
 * Словари русских слов для игры «Анаграммы».
 *
 * Принципы составления:
 * - только существительные в именительном падеже единственного числа;
 * - повседневная лексика без редких, устаревших или специальных слов —
 *   спросонья игрок должен узнавать слово сразу;
 * - строчные буквы, буква «ё» не заменяется на «е»;
 * - длина слова строго соответствует уровню сложности:
 *   [EASY] — 4–5 букв, [MEDIUM] — 6–7 букв, [HARD] — 8 и более букв;
 * - в каждом списке не меньше 25 слов и нет дубликатов;
 * - у каждого слова есть хотя бы две разные буквы, чтобы перемешивание
 *   могло дать порядок, отличный от исходного.
 */
object AnagramWords {

    /** Уровень 1: короткие слова из 4–5 букв. */
    val EASY: List<String> = listOf(
        "вода", "рука", "нога", "окно", "стол",
        "стул", "хлеб", "море", "зима", "лето",
        "гора", "рыба", "утро", "небо", "снег",
        "каша", "лицо", "школа", "книга", "весна",
        "осень", "город", "поезд", "чашка", "ложка",
        "вилка", "лампа", "полка", "сумка", "земля",
        "трава", "кухня", "улица"
    )

    /** Уровень 2: слова средней длины из 6–7 букв. */
    val MEDIUM: List<String> = listOf(
        "работа", "дорога", "молоко", "погода", "минута",
        "неделя", "музыка", "письмо", "яблоко", "облако",
        "солнце", "воздух", "куртка", "гитара", "собака",
        "корова", "лошадь", "медведь", "картина", "комната",
        "магазин", "человек", "телефон", "автобус", "капуста",
        "колбаса", "конфета", "тарелка", "подушка", "рубашка",
        "морковь"
    )

    /** Уровень 3: длинные слова из 8 и более букв. */
    val HARD: List<String> = listOf(
        "квартира", "лестница", "карандаш", "апельсин", "мандарин",
        "виноград", "институт", "праздник", "прогулка", "ресторан",
        "вертолёт", "простыня", "будильник", "телевизор", "компьютер",
        "велосипед", "картофель", "остановка", "программа", "профессия",
        "космонавт", "полотенце", "сковорода", "мороженое", "автомобиль",
        "расписание", "фотография", "библиотека", "тренировка", "настроение",
        "путешествие", "холодильник"
    )

    /**
     * Возвращает словарь для уровня сложности.
     *
     * @param difficulty уровень сложности 1..3; значения вне диапазона
     * приводятся к ближайшему уровню.
     */
    fun forDifficulty(difficulty: Int): List<String> = when {
        difficulty <= 1 -> EASY
        difficulty == 2 -> MEDIUM
        else -> HARD
    }
}
