package com.example.smartalarm.feature.games.oddoneout

/**
 * Пара визуально похожих эмодзи для игры «Найди лишнее».
 *
 * В раунде один символ пары заполняет всю сетку, а второй появляется ровно
 * в одной ячейке — его и нужно найти. Какой из двух символов «основной»,
 * а какой «лишний», выбирается случайно при генерации раунда.
 *
 * @property first первый символ пары.
 * @property second второй символ пары, визуально похожий на [first].
 * @property similarity насколько символы похожи друг на друга:
 * 1 — отличия заметные, 2 — средние, 3 — самые похожие пары.
 * Значение совпадает со сложностью игры, на которой используется пара.
 */
data class EmojiPair(
    val first: String,
    val second: String,
    val similarity: Int,
)

/**
 * Пул пар похожих эмодзи, по пять пар на каждый уровень похожести (1..3).
 *
 * На сложности N игра использует только пары с `similarity == N`:
 * лёгкий уровень — заметные отличия (морда на луне, панда против коалы),
 * сложный — почти неотличимые пары (😀/😃, зеркальные месяцы 🌛/🌜).
 */
val EMOJI_PAIRS: List<EmojiPair> = listOf(
    // Уровень 1 — заметные отличия.
    EmojiPair("🌕", "🌝", 1), // 🌕 / 🌝 — луна и луна с лицом
    EmojiPair("🐨", "🐼", 1), // 🐨 / 🐼 — коала и панда
    EmojiPair("🙈", "🙉", 1), // 🙈 / 🙉 — обезьянки (глаза/уши)
    EmojiPair("👏", "🙌", 1), // 👏 / 🙌 — аплодисменты и поднятые руки
    EmojiPair("🦁", "🐯", 1), // 🦁 / 🐯 — лев и тигр

    // Уровень 2 — средняя похожесть.
    EmojiPair("😺", "😸", 2), // 😺 / 😸 — улыбающиеся коты
    EmojiPair("⭐", "🌟", 2),       // ⭐ / 🌟 — звезда и сияющая звезда
    EmojiPair("🌸", "💮", 2), // 🌸 / 💮 — сакура и белый цветок
    EmojiPair("😴", "😪", 2), // 😴 / 😪 — спящий и сонный
    EmojiPair("🐭", "🐹", 2), // 🐭 / 🐹 — мышь и хомяк

    // Уровень 3 — самые похожие пары.
    EmojiPair("😀", "😃", 3), // 😀 / 😃 — улыбки с разными глазами
    EmojiPair("😄", "😁", 3), // 😄 / 😁 — смеющиеся лица
    EmojiPair("😮", "😯", 3), // 😮 / 😯 — удивлённые лица
    EmojiPair("😑", "😐", 3), // 😑 / 😐 — без эмоций (глаза-чёрточки/точки)
    EmojiPair("🌛", "🌜", 3), // 🌛 / 🌜 — зеркальные месяцы с лицом
)
