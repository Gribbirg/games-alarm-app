package com.example.smartalarm.feature.games.counter

/**
 * Группа визуально похожих эмодзи для игры «Сосчитай».
 *
 * Виды для раунда набираются из групп в зависимости от сложности:
 * на лёгкой — по одному виду из разных групп (виды заметно отличаются),
 * на высокой — все виды из одной группы (виды похожи друг на друга,
 * считать сложнее).
 *
 * @property name человекочитаемое название группы (для документации и логов).
 * @property emojis эмодзи группы; внутри группы все символы визуально похожи
 * и попарно различны.
 */
data class EmojiGroup(
    val name: String,
    val emojis: List<String>,
)

/**
 * Сколько видов эмодзи нужно набрать из одной группы на максимальной
 * сложности — значит, в каждой группе пула должно быть не меньше
 * этого количества символов.
 */
const val KINDS_ON_MAX_DIFFICULTY = 5

/**
 * Пул групп похожих эмодзи, по [KINDS_ON_MAX_DIFFICULTY] символов в каждой.
 *
 * Все эмодзи пула глобально уникальны (символ входит ровно в одну группу) —
 * на этом строится проверка принадлежности вида группе в тестах.
 */
val COUNTER_EMOJI_GROUPS: List<EmojiGroup> = listOf(
    EmojiGroup("красное и круглое", listOf("🍎", "🍅", "🔴", "🎈", "🍓")),
    EmojiGroup("морды зверей", listOf("🐶", "🐺", "🦊", "🐱", "🐯")),
    EmojiGroup("мячи", listOf("⚽", "🏐", "🏀", "⚾", "🎱")),
    EmojiGroup("улыбки", listOf("😀", "😃", "😄", "😁", "🙂")),
    EmojiGroup("цветы", listOf("🌸", "💮", "🌼", "🌻", "🌺")),
    EmojiGroup("деревья", listOf("🌲", "🌳", "🌴", "🎄", "🌵")),
    EmojiGroup("сердца", listOf("❤️", "🧡", "💛", "💚", "💙")),
    EmojiGroup("луны", listOf("🌕", "🌝", "🌛", "🌜", "🌙")),
)
